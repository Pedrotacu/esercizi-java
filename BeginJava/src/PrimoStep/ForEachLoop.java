package PrimoStep;
// Esiste anche un ciclo " for-each ", che viene utilizzato esclusivamente per scorrere gli elementi di un array : 
public class ForEachLoop {

	public static void main(String[] args) {
		// L'esempio seguente mostra tutti gli elementi dell'array car , usando un ciclo " for-each ":
		String[] cars = {"Ford", "Ferrari", "Lamborghini", "Mercedes"};
		for (String i : cars) {
			System.out.println(i);
		}

	}

}
