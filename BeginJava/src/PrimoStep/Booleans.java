package PrimoStep;

public class Booleans {

	public static void main(String[] args) {
		
		// VALORI BOOLEANI
		boolean mouse = true;
		boolean tastiera = false;
		System.out.println(mouse);
		System.out.println(tastiera);
		
	System.out.println("-------");
		
		// ESPRESSIONE BOOLEANA
		int x = 2;
		int y = 6;
		System.out.println(y < x);  //or...
		System.out.println(9 > 5);
		
	System.out.println("-------");
		
		// Usiamo nell'espressione l'operatore == "uguale a-equal to"
		int w = 80;
		System.out.println(w == 80); //or...
		System.out.println(80 == 80);
	}

}
