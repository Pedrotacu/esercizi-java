package PrimoStep;

public class ClassMath {

	public static void main(String[] args) {

		// il metodo Math.max consente di stampare il numero massimo
		System.out.println(Math.max(10, 2));
		
		// il metodo Math.min consente di stampare il numero minimo
		System.out.println(Math.min(50, 80));
		
		// il metodo Math.sqrt consente di stampare la radice quadrata
		System.out.println(Math.sqrt(20));
		
		// il metodo Math.abs consente di stampare il valore assoluto
		System.out.println(Math.abs(-516));
		
		// il metodo Math.random consente di restituire un valore da 0 a 1
		System.out.println(Math.random());
	}

}
